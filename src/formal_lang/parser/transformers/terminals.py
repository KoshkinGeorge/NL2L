from formal_lang.lark import Transformer

class NumberTransformer(Transformer):
    """Базовый класс с общими преобразованиями для всех терминальных символов"""
    
    def SYMBOL(self, token):
        
        """Преобразует русские названия символов в LaTeX"""
        greek_map = {
            # Греческие буквы
            "альфа": "\\alpha", "бета": "\\beta", "гамма": "\\gamma",
            "дельта": "\\delta", "эпсилон": "\\epsilon", "дзета": "\\zeta",
            "эта": "\\eta", "тета": "\\theta", "йота": "\\iota",
            "каппа": "\\kappa", "лямбда": "\\lambda", "мю": "\\mu",
            "ню": "\\nu", "кси": "\\xi", "омикрон": "o",
            "пи": "\\pi", "ро": "\\rho", "сигма": "\\sigma",
            "тау": "\\tau", "ипсилон": "\\upsilon", "фи": "\\phi",
            "хи": "\\chi", "пси": "\\psi", "омега": "\\omega",
            "икс": "x", "игрек": "y", "зет": "z",
            "а": "a", "бэ": "b", "цэ": "c", "дэ": "d",
            "эф": "f", "ка": "k", "эн": "n", "эм": "m",
        }
        val = token.value.lower()
        return greek_map.get(val, val)
    
    def NUMBER(self, token):
        """Преобразует числа прописью в цифры"""
        num_map = {
            "один": "1", "два": "2", "три": "3", "четыре": "4",
            "пять": "5", "шесть": "6", "семь": "7", "восемь": "8",
            "девять": "9", "десять": "10",
            "одиннадцать": "11", "двенадцать": "12", "тринадцать": "13",
            "четырнадцать": "14", "пятнадцать": "15",
        }
        return num_map.get(token.value.lower(), token.value)
    
    def DIGIT(self, token):
        """Цифры оставляем как есть"""
        return token.value
    
    def OPER(self, token):
        """Операторы (плюс, минус и т.д.)"""
        op_map = {
            "плюс": "+",
            "минус": "-",
            "умножить": "\\cdot",
            "разделить": "/",
        }
        return op_map.get(token, token)
